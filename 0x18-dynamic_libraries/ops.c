int add(int i, int k);
int sub(int i, int k);
int mul(int i, int k);
int div(int i, int k);
int mod(int i, int k);


int add(int i, int k)
{
	return (i + k);
}

int sub(int i, int k)
{
	return (i - k);
}
 

int mul(int i, int k)
{
	return (i * k);
}

 

int div(int i, int k)
{
	return (i / k);
}

 

int mod(int i, int k)
{
	return (i % k);
}
